    <h1>Minhas Trajetórias</h1>

    <div>
        <div class="card-trajetoria">
            <p>Na política, começou a militância no movimento estudantil, em 1984. Foi presidente do Diretório Acadêmico de Engenharia e Computação da UFPE em 1985, dirigente do DCE e vice-presidente regional da União Nacional dos Estudantes, UNE,  de 1989 a 1991.</p>
        </div>

        <div class="card-trajetoria">
            <p>
Em 1998, foi reeleita deputada estadual. Na Assembleia Legislativa foi líder do PCdoB e fez parte das comissões de Ciência, Tecnologia e Informática, Constituição, Legislação e Justiça, Educação e Cultura,  Defesa do Meio Ambiente e presidiu a Comissão de Defesa da Cidadania.</p>
        </div>

        <div class="card-trajetoria">
            <p>Em 2019 assumiu a Vice-Governadoria do estado de Pernambuco. No governo fez questão de deixar a sua marca, desenvolvendo – entre outas atividades – a ação Pernambuco com Elas que buscou fortalecer e ampliar políticas públicas de trabalho e renda para mulheres.</p>
        </div>

        <div class="card-trajetoria">
            <p>Em 2000 foi eleita prefeita de Olinda, sendo reeleita em primeiro turno em 2004. Por suas administrações recebeu o prêmio Prefeita Amiga da Criança da Fundação Abrinq, prêmio de Expressão em Administração da Casa do Administrador de Pernambuco (CAPE) e do jornal Diário de Pernambuco, e o prêmio Governador Barbosa Lima Sobrinho na categoria Prefeito Empreendedor.</p>
        </div>

        <div class="card-trajetoria">
            <p>Em 2009 foi nomeada pelo então governador Eduardo Campos, secretária de Ciência, Tecnologia e Meio Ambiente de Pernambuco. Em sua atuação, buscou imprimir uma agenda de desenvolvimento sustentável, investindo na inovação tecnológica como forma de desenvolvimento e de redução das desigualdades sociais.</p>
        </div>

        <div class="card-trajetoria">
            <p>Em 2010 foi eleita deputada federal. Na Câmara foi titular da Comissão de Ciência e Tecnologia, Comunicação e Informática, de Desenvolvimento Urbano e da Comissão de Cultura, da qual foi vice-presidente. foi membro titular na de Resíduos Sólidos; do Código Nacional de Ciência e Tecnologia; de Atividades de Ciência, Tecnologia e Inovação e de Proteção à Saúde e ao Meio Ambiente. Nos oito anos de mandato foi na maior parte do período a única mulher da bancada do Estado no Congresso Nacional.</p>
        </div>

        <div class="card-trajetoria">
            <p>Em 2019 assumiu a Vice-Governadoria do estado de Pernambuco. No governo fez questão de deixar a sua marca, desenvolvendo – entre outas atividades – a ação Pernambuco com Elas que buscou fortalecer e ampliar políticas públicas de trabalho e renda para mulheres.</p>
        </div>

    </div>
